// Função global para aplicar traduções
function applyTranslations(lang) {
    const langData = translations[lang] || translations['pt-BR'];
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (langData[key]) {
            // Verificar se é um input com placeholder
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = langData[key];
            } else if (el.tagName === 'OPTION') {
                el.textContent = langData[key];
            } else {
                el.textContent = langData[key];
            }
        }
    });

    // Atualizar atributo lang do HTML
    document.documentElement.lang = lang;
}

// Aplicar traduções ao carregar a página
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'pt-BR';
    applyTranslations(savedLang);
});

// Exportar para uso global
window.translations = translations;
window.applyTranslations = applyTranslations;
